#include<stdio.h> /*標頭檔*/


int main() /*主函式*/
{
    int n1,n2,n3;                                  /*宣告變數n1 n2 n3*/
    printf("公告小明的成績為:n 90 75\n");          /*印出"公告小明的成績為:n 90 75*/
    printf("欲修正的成績為:");                     /*印出"欲修正的成績為:"*/
    scanf("%d %d %d",&n1,&n2,&n3);                 /*提取輸入的數值 並依序令其為n1 n2 n3之數值*/
    printf("修正過後的成績為:%d %d %d",n1,n2,n3);  /*印出"修正過後的成績為:(n1值) (n2值) (n3值)"*/

    return 0;  /*回傳值0*/
}
