#include<stdio.h> /*標頭檔*/

int main() /*主函式*/
{
    int a; /*宣告a為整數變數*/
    printf("請輸入密碼:"); /*印出"請輸入密碼*/
    scanf("%d",&a); /*提取輸入的變數 並令其為a值*/

    if(a == 1081) /*若a為1081*/
        printf("歡迎進入\n"); /*印出"歡迎進入" 並換行*/

    else /*a為1081不為真*/
        printf("密碼錯誤\n"); /*印出"密碼錯誤" 並換行*/

    return 0; /*回傳值0*/
}
