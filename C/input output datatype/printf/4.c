#include<stdio.h> /*引入標頭檔*/

int main() /*主函式*/
{
    int a=90,b=75; /*宣告敘述 國文上學期a=90分 下學期b=75分*/
    double c= 53.90,d=48.50; /*宣告敘述 微積分上學期c=53.90分 下學期d=48.50分*/
    printf("以下為小民的成績：\n\n"); /* 印出"以下為小民的成績：" 並換兩行*/
    printf("國文兩學期成績之和為%d+%d=%d分\n",a,b,a+b); /*印出"國文兩學期成績之和" 並換行*/
    printf("微積分學期平均成績為(%.2f+%.2f)/2=%.2f分\n",c,d,(c+d)/2); /*印出"微積分學期平均成績" 並換行*/
    return 0;/*回傳值0*/
}
