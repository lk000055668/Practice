#include<stdio.h> /*標頭檔*/

int main() /*主函式*/
{
    int num1; /*宣告變數*/
    printf("請輸入任一數字(1~100):"); /*印出"請輸入任一數字(1~100):*/
    scanf("%d",&num1);                /*提取輸入的變數 並令其為num1之值*/
    printf("設總寬度為5:");           /*印出"設總寬度為5"*/
    printf("|%.5d|\n",num1);          /*印出"|(寬度為五的num1數值)|"並換行*/
    printf("八進位為:%o\n",num1);     /*印出"八進位為:(顯示num1的八進位值)"並換行*/
    printf("十六進位為:%x",num1);     /*印出"十六進位為:(顯示num2的十六進位值)"*/

    return 0;  /*回傳值0*/
}
